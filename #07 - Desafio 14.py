'''Escreva um programa que converta uma tempratura ddigitada em graus Celsius para Fahrenheit '''
gCelsius = float (input('Digite quantos graus Celsius você quer converter para Fahrenheit: º'))
Fahrenheit = (gCelsius*9+160)/5
print('{:.2f}ºC é igual a {}ºF.'.format(gCelsius, Fahrenheit))#Prestar atenção na simplificação das equações